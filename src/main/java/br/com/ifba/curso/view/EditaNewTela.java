    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
                        
        int linhaSelecionada = tblEditar.getSelectedRow();
        if (linhaSelecionada != -1) {

            long idCurso = (long) tblEditar.getValueAt(linhaSelecionada, 0);
        

            CursoController cursoDao = new CursoController();
            Curso curso = cursoDao.findById(idCurso);
        

            curso.setNome(txtNome3.getText());
            curso.setCodigoCurso(txtCod2.getText());
            cursoDao.update(curso);
            
            JOptionPane.showMessageDialog(this, "Curso atualizado com sucesso.");
        } else {
        
            JOptionPane.showMessageDialog(this, "Selecione um curso na tabela para editar.");
        }    
    }//GEN-LAST:event_btnEditarActionPerformed
    
    private void mostrarTabela () {
        CursoController cursoDao = new CursoController();
        List<Curso> cursos = cursoDao.findAll();
   
        DefaultTableModel tableModel = (DefaultTableModel) tblEditar.getModel();
        tableModel.setRowCount(0);

        // Adiciona os cursos Ã  tabela
        for (Curso curso : cursos) {
            tableModel.addRow(new Object[]{
            curso.getId(),
            curso.getNome(),
            curso.getCodigoCurso(),
            curso.getAtivo()
            });
        }
    }
     
